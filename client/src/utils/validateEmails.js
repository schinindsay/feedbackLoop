//these come from http://emailregex.com and ensure correct email formatting
const re = /^[a-zA-Z0-9.!#$%&â€™*+/=?^_`{|}~-]+@[a-zA-Z0-9-]+(?:\.[a-zA-Z0-9-]+)*$/;

//take in emails
//split the array of emails by comma
//use trim to kill any spaces in the email
//use filter to return invalid email to it's own invalidEmails array
export default emails => {
  const invalidEmails = emails
    .split(',')
    .map(email => email.trim())
    .filter(email => re.test(email) === false);

  if (invalidEmails.length) {
    return `These emails are invalid: ${invalidEmails}`;
  }

  return;
};